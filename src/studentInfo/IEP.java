package studentInfo;

public class IEP {

	private String aDHD;
	private String pTSD;
	private String visualImpairment;
	private String hearingImpairment;
	private String learningDisability;
	private String autism;
	
	public String getADHD() {
		return aDHD;
	}
	public void setADHD(String aDHD) {
		aDHD = aDHD;
	}
	public String getPTSD() {
		return pTSD;
	}
	public void setPTSD(String pTSD) {
		pTSD = pTSD;
	}
	public String getVisualImpairment() {
		return visualImpairment;
	}
	public void setVisualImpairment(String visualImpairment) {
		this.visualImpairment = visualImpairment;
	}
	public String getHearingImpairment() {
		return hearingImpairment;
	}
	public void setHearingImpairment(String hearingImpairment) {
		this.hearingImpairment = hearingImpairment;
	}
	public String getLearningDisability() {
		return learningDisability;
	}
	public void setLearningDisability(String learningDisability) {
		this.learningDisability = learningDisability;
	}
	public String getAutism() {
		return autism;
	}
	public void setAutism(String autism) {
		this.autism = autism;
	}


}
